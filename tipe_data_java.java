public class tipe_data_java {
    

    public static void main(String[] args) {
        System.out.println("Hello, mentor!");
        
    //TIPE DATA
    //Int : penulisan angka
    int bilangan = 8;
    System.out.println(bilangan);
    
    //Char : penulisan karakter atau huruf
    char x = '#';
    System.out.println(x);
    
    //Float : untuk bilangan desimal
    float y = 3.5f;
    System.out.println(y);
   
    //Double : bilangan desimal lebih besar dari float
    double m = 9.77;
    System.out.println(m);
    
    //String : penulisan kalimat
    String myName = "Silvia Nurul Hasanah";
    System.out.println(myName);
    
    //Boolean : Tipe data bernilai true atau false
    int bb_jono = 80;
    int bb_joko = 81;
    boolean perbandingan = bb_joko < bb_jono;
    System.out.println(perbandingan); 

    }
}